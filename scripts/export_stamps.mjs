// ===== 生成 =====
function match(text, dicts) {
  for (const d of dicts) { if (d.re.test(text)) return d; }
  return null;
}

const usedCombos = new Set();
const results = [];

for (const st of stations) {
  const text = `${st.name} ${st.location} ${st.pref}`;
  const p = PREF[st.pref] || {n:"里山の風景",c:"道標と石畳",g:"山菜と漬物",h:"古い道標",a:"小鳥のさえずり"};

  // 5カテゴリからモチーフ抽出
  const natureHit = match(text, NATURE);
  const cultureHit = match(text, CULTURE);
  const gourmetHit = match(text, GOURMET);
  const animalHit = match(text, ANIMAL);
  const historyHit = match(text, HISTORY);

  let artTheme = natureHit ? natureHit.pool[st.id % natureHit.pool.length] : p.n;
  let mainSymbol = cultureHit ? cultureHit.pool[st.id % cultureHit.pool.length]
                 : historyHit ? historyHit.pool[st.id % historyHit.pool.length]
                 : (natureHit && artTheme !== p.n) ? artTheme : p.c;
  let secondarySymbol = animalHit ? animalHit.pool[st.id % animalHit.pool.length]
                      : gourmetHit ? gourmetHit.pool[st.id % gourmetHit.pool.length] : p.a;

  // 重複回避
  let combo = `${mainSymbol}|${secondarySymbol}`;
  let att = 0;
  const alts = [p.n, p.c, p.g, p.h, p.a];
  while (usedCombos.has(combo) && att < 20) {
    att++;
    if (att % 4 === 0) mainSymbol = `${alts[att % alts.length]}（${st.location}の眺め）`;
    else if (att % 4 === 1) secondarySymbol = `${alts[att % alts.length]}と${st.name}の旅路`;
    else if (att % 4 === 2) artTheme = `${alts[att % alts.length]}に包まれた${st.name}`;
    else mainSymbol = `${st.name}から望む${alts[att % alts.length]}`;
    combo = `${mainSymbol}|${secondarySymbol}`;
  }
  if (usedCombos.has(combo)) {
    mainSymbol = `道の駅${st.name}の風景画`;
    secondarySymbol = `${st.location}の季節の移ろい`;
    combo = `${mainSymbol}|${secondarySymbol}`;
  }
  usedCombos.add(combo);

  // レアリティ
  let rarity = "common", color_theme = "#2D5A3D";
  if (LEGENDARY_RE.test(text)) { rarity = "legendary"; color_theme = "#B8860B"; }
  else if (EPIC_RE.test(text)) { rarity = "epic"; color_theme = "#6A3D9A"; }
  else if (RARE_RE.test(text)) { rarity = "rare"; color_theme = "#1B4F72"; }

  // 構図
  const composition = COMP[st.id % COMP.length];

  // シリーズ
  let series = "";
  for (const sr of SERIES) { if (sr.re.test(text)) { series = sr.name; break; } }

  // 犬/RV
  let dog_friendly_icon = "";
  if (/犬|ドッグ|ペット|わんこ/.test(text)) dog_friendly_icon = "paw_large";
  else if (st.id % 3 === 0) dog_friendly_icon = "paw";

  let rv_icon = "";
  if (/RV|キャンプ|オート|車中泊/.test(text)) rv_icon = "campervan";
  else if (/温泉|湯/.test(text)) rv_icon = "hotspring";
  else if (st.id % 4 === 0) rv_icon = "power";

  // レアリティ効果
  const fx = {legendary:"金の箔押し、外縁に金彩装飾、光沢感のある線",epic:"紫のグラデーション縁、幻想的な微光、繊細な銀線",rare:"藍色の濃淡インク、深みのある陰影、細密な波線",common:"深緑の温かみあるインク、素朴な手描き線、点描の陰影"};

  const stamp_prompt = [
    `円形スタンプ（直径40mm）。手描き風モノクロ線画、実際の消しゴムはんこやスタンプとして成立する品質。`,
    `メインモチーフ(70%): ${mainSymbol}。繊細な線画で旅情感と温かみを表現。`,
    `サブモチーフ: ${secondarySymbol}。メインを引き立てる控えめな配置。`,
    `駅名「${st.name}」を弧状または帯状に配置(20%)。読みやすい明朝体風。`,
    `装飾(10%): ${st.pref}の風土を感じる繊細な点描・線の装飾。`,
    `構図: ${composition}`,
    `テイスト: 旅行雑誌の表紙レベルの品質。日本の原風景、ノスタルジック、温かみ。収集したくなる美しさ。SNSに載せたくなる完成度。`,
    `レアリティ: ${rarity}（${fx[rarity]}）。`,
    `モノクロでも視認可能であること。全駅並べた時に統一感がありつつ個性が際立つこと。`
  ].join(' ');

  results.push({
    id: st.id,
    station_name: st.name,
    pref: st.pref,
    location: st.location,
    art_theme: artTheme,
    main_symbol: mainSymbol,
    secondary_symbol: secondarySymbol,
    rarity,
    color_theme,
    series,
    dog_friendly_icon,
    rv_icon,
    composition,
    stamp_prompt
  });
}

// 統計
const stats = {total:results.length, common:0, rare:0, epic:0, legendary:0, unique: usedCombos.size};
results.forEach(r => stats[r.rarity]++);
console.log(JSON.stringify(stats));

// 出力
writeFileSync(join(__dirname, '..', 'stamp_catalog.json'), JSON.stringify(results, null, 2), 'utf-8');
console.log(`stamp_catalog.json written (${results.length} stamps)`);
